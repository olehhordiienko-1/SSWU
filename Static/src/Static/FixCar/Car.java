package Static.FixCar;

public class Car {
    String carColor;
    String carModel;
    boolean carBreakdown;
    int carNumber;
    String carOwner;

    public Car(String color, String name, boolean breakdown, int number, String owner) {
        carColor = color;
        carModel = name;
        carBreakdown = breakdown;
        carNumber = number;
        carOwner = owner;
    }

}
