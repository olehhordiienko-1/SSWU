package MVC.TrianglesMVC;

public class Rocket {
    int sizeOfRocket;
    int rightShiftOfRocket;
    char characterOfRocket;
    char whitespaceOfRocket;

    public Rocket(int size, int rightShift, char character) {
        sizeOfRocket = size;
        rightShiftOfRocket = rightShift;
        characterOfRocket = character;
        whitespaceOfRocket = ' ';
    }
}
